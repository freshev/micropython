static mp_obj_t modcellular_poll_network_exception(void) {
    // ========================================
    // Raises a last network exception.
    // ========================================
    uint32_t e = network_exception;
    network_exception = NTW_NO_EXC;

    switch (e) {
        case NTW_NO_EXC:
            break;
        default:
            mp_raise_OSError(e);
            break;
    }
    return mp_const_none;
}

